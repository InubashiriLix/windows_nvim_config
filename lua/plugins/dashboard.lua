            for i, l in ipairs(header) do
                header_plain[i] = l:gsub("%s+", "")
            end

            ----------------------------------------------------------
            -- 着色函数：只作用在 #1
            ----------------------------------------------------------
            local function repaint(bufnr)
                if vim.bo[bufnr].filetype ~= "dashboard" then
                    return
                end
                local lines = vim.api.nvim_buf_get_lines(bufnr, 0, -1, false)

                for row, text in ipairs(lines) do
                    local plain = text:gsub("%s+", "")
                    for idx, p in ipairs(header_plain) do
                        if plain == p then
                            local hl = (idx == #header_plain) and "DashBlood" or "DashRed"
                            vim.api.nvim_buf_add_highlight(bufnr, 0, hl, row - 1, 0, -1)
                            break
                        end
                    end
                end
            end

